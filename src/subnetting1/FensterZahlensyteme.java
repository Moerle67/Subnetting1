    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // 
        boolean istFehler;
        SystByte dec, hex, bin;
        for (int zeile = 0; zeile < 12; zeile++) {
            istFehler = false;
            // DEC
            try {
                dec = new SystByte((int) jTable1.getValueAt(zeile, 0));
                if (!listeByte[zeile].equals(dec)) {
                    msg("Zeile " + (zeile + 1) + ", Dezimalwert falsch: " + dec+"/"+listeByte[zeile]);
                    istFehler = true;
                }
                } catch (Exception e) {
                msg("Zeile " + (zeile + 1) + ", Dezimalwert nicht erkannt.");
                istFehler = true;
            }

            // HEX
            try {
                hex = new SystByte((String) jTable1.getValueAt(zeile, 1), 16);
                if (!listeByte[zeile].equals(hex)) {
                    msg("Zeile " + (zeile + 1) + ", Hexadezimalwert falsch: " + hex +"/"+listeByte[zeile]);
                    istFehler = true;
                }
            } catch (Exception e) {
                msg("Zeile " + (zeile + 1) + ", Hexadezimalwert nicht erkannt.");
                istFehler = true;
            }
            // BIN
            try {
                bin = new SystByte((String) jTable1.getValueAt(zeile, 2), 2);
                if (!listeByte[zeile].equals(bin)) {
                    msg("Zeile " + (zeile + 1) + ", Binärwert falsch: " + bin+"/"+listeByte[zeile]);
                    istFehler = true;
                }
            } catch (Exception e) {
                msg("Zeile " + (zeile + 1) + ", Binärwert nicht erkannt.");
                istFehler = true;
            }
            if (istFehler) {
                ergListe[zeile].setText("X, "+listeByte[zeile].getDec());
            } else {
                ergListe[zeile].setText("V");
            }
            this.pack();
            System.out.println("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

