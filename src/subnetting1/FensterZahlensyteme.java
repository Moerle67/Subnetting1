    private void clearTabelle() {
        for (JLabel feld : ergListe) {
            feld.setText("");
        }
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            for (int j = 0; j < jTable1.getColumnCount(); j++) {
                jTable1.setValueAt("", i, j);
            }
        }

    }

    public void initListeByte() {
        int spalte;
        for (int i = 0; i < 12; i++) {
            this.listeByte[i] = new SystByte((int) (Math.random() * 256));
        }
        this.clearTabelle();
        for (int i = 0; i < 12; i++) {
            spalte = (int) (Math.random() * 3);
            switch (spalte) {
                case 0:        // Erste Spalte --> Dec
                    this.jTable1.setValueAt(this.listeByte[i].getDec(), i, 0);
                    //this.setCellColor(i, 0, Color.lightGray);
                    break;
                case 1:        // Zweite Spalte --> Hex
                    this.jTable1.setValueAt(this.listeByte[i].getHex(), i, 1);
                    break;
                case 2:        // Dritte Spalte --> Bin
                    //this.jTable1.setValueAt(this.listeByte[i].getDec(), i, 0);
                    this.jTable1.setValueAt(this.listeByte[i].getBin(), i, 2);
                    break;

            }
        }
    }

    void setCellColor(int zeile, int spalte, Color color) {
        TableCellRenderer tableCellRenderer = jTable1.getCellRenderer(zeile, spalte);
        Component c = jTable1.prepareRenderer(tableCellRenderer, zeile, spalte);
        c.setBackground(color);

    }

